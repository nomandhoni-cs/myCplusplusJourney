#include <iostream>
using namespace std;
int main()
{
int a, b;
	cout<<"Enter Value of a "<<endl;
	cin>>a;
	cout<<"Enter Value of b "<<endl;
	cin>>b;
//Output Section	
	cout<<"The value of a+b "<<a+b<<endl;
	cout<<"The value of a-b "<<a-b<<endl;
	cout<<"The value of a*b "<<a*b<<endl;
//Here I used "(float)" to define this will be float output.
	cout<<"The value of a/b "<<(float)a/b<<endl;
//Here "%" means how much you have after divition.
	cout<<"The value of a%b "<<a%b<<endl;
return 0;
	}
